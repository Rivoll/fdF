#include "include/libft.h"

void		ft_deltab2(void ***tab, int size)
{
	int		i;

	i = 0;
	while (i < size)
	{
		free((*tab)[i]);
		(*tab)[i++] = NULL;
	}
	free(*tab);
	*tab = NULL;
}
